public class Address {
    private String primaryAddress;
    private String evacuationCenterAddress;

    // Getters and setters
    public String getPrimaryAddress() {
        return primaryAddress;
    }

    public void setPrimaryAddress(String primaryAddress) {
        this.primaryAddress = primaryAddress;
    }

    public String getEvacuationCenterAddress() {
        return evacuationCenterAddress;
    }

    public void setEvacuationCenterAddress(String evacuationCenterAddress) {
        this.evacuationCenterAddress = evacuationCenterAddress;
    }
}





